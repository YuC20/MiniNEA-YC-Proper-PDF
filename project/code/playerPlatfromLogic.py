    player_on_plat_x = playerx[xcoordPlayer] == (plat_values["x"] + 2)
    player_on_plat_y = (playery[ycoordPlayer] >= plat_values["y"] + 2) and (playery[ycoordPlayer] <= plat_values["y"] + 3)
    
